                <h4> HISTORY </h4>
                <p> Facts and Information About the Game </p>
                <p> The origins of the game of badminton date back at least 2,000 years to the game of battledore and shuttlecock played in ancient Greece, China, and India. </p>
                <p> A very long history for one of the Olympics newest sports! Badminton took its name from Badminton House in Gloucestershire, the ancestral home of the Duke of Beaufort, where the sport was played in the last century. Gloucestershire is now the base for the International Badminton Federation. </p>
                <p> The IBF was formed in 1934 with nine members: Canada, Denmark, France, Netherlands, England, New Zealand, Ireland, Scotland, and Wales. The United States joined four years later. Membership increased steadily over the next few years with a surge in new members after the Olympic Games debut at Barcelona. </p>
                <p> The first big IBF tournament was the Thomas Cup (men’s world team championships) in 1948. Since then, the number of world events has increased to seven, with the addition of the Uber Cup (ladies’ team), World Championships, Sudirman Cup (mixed team), World Juniors, World Grand Prix Finals, and the World Cup. </p>
                <p> The World Cup invitational event started in 1981 and is organized by the International Management Group (IMG). The World Cup series is due to end in 1997, and the IBF is considering organizing exhibition matches featuring the world’s top players to replace the World Cup. </p>
                <p> For the recent Thomas and Uber Cups in Hong Kong, the sale of commercial and television rights was a multimillion dollar contract. And it’s not just in Asia. In Europe also, there’s a growing number of companies bidding for rights. Television companies worldwide are already buying exclusive rights to the 1997 World Championships to be held in Glasgow, Scotland. </p>
                <p> A turning point in badminton’s growth was the $20 million tripartite contract in 1994 for sponsorship of the World Grand Prix Finals. Under the terms of the deal between the IBF, IMG, and STAR TV, STAR injects the monies into the promotion and development of badminton. In return, STAR gains total exclusivity for the exploitation of the commercial and television rights to the WGP Finals. “The deal was good for both main parties,” said David Shaw, IBF’s executive director, who was brought into the organization with a brief to grow the sport. “We needed a strong partner in television, and the broadcaster had identified badminton as a vehicle which would attract audiences across Asia to its prime sports channel.” </p>
                <p> The next phase in the rise and rise of international badminton has been to retake the USA. The U.S. was an early member of the IBF and initially one of the most successful. When the Uber Cup was introduced in 1956, Americans won the first three events. But then interest waned. </p>
                <p> Badminton is a well liked and familiar sport in the USA but, predominantly, is usually played as a fun game in the backyard or on the beach. We know that once Americans see the other badminton—international badminton, the world’s fastest racket sport—they will want to see and play more. The Atlanta Olympics started to raise the sport’s profile in the U.S. The event was a sell-out and became one of the “must-see” sports. Ex-President Jimmy Carter, Chelsea Clinton, Paul Newman, and Princess Anne were among the celebrities who came to watch. David Broder of the Washington Post reported “seeing one of the supreme athletic spectacles of my life.” </p>
                <p> The year 1996 was a landmark in USA badminton. It’s not only the Atlanta Olympic Games that started to generate tremendous interest in the U.S. market. In December 1995, the IBF introduced a new concept tournament in California, the Hong Ta Shan Cup, a men’s invitation tournament with the best players and big prize money. There are now plans to add a women’s event and to increase the prize money. The Hong Ta Shan Group has gone on to sponsor the U.S. Open, increasing the prize money to $200,000. This makes the event the most valuable World Grand Prix event in the series and gives it six-star status. </p>


