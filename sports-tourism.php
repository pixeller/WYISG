                    landscape of nature. Malaysian Batik is renowned for its vibrant colours, bold prints and its versatility. It is soft, light and breezy and very well
                    suited for summer and tropical climate.
                </p>
                <p>
                    Batik fabric is made into shirts, dresses, crepe de chine, scarves, kaftans, sarongs, pillow cases, bags, table clothes and many more items. The production
                    of Batik involves several processes – dyeing, waxing, and drying are used to create deep and rich colourful patterns on a piece of fabric. The East Coast
                    is home to the craft of batik making. New dyes and techniques have also enabled Batik to be printed onto various materials such as swiss voile, silk,
                    cheese cloth, velvet and satin. Many contemporary designers also incorporate elements of this ancient craft into their colourful creations.
                </p>
                <p>
