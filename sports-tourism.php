                <h4>Why Sports Tourim in Malacca</h4>
                <p><strong>1. MALACCA TOURIST INFORMATION CENTRE</strong></p>
                <p>
                    Your journey, very appropriately begins here. Centrally located in Old Malacca, the Malacca Tourist Centre lies between Chinatown and Dutch Square. From
                    here your first destination is a bridge across the Malacca River.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>2. MALACCA RIVER </strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st02.jpg"></p>
                <p>
                    As you cross the bridge, look to tour left. In the distance is the rivermouth which once welcomed vessels of all shapes and sizes. Imagine, it was then a
                    busy,noisy loading dock, crowded with mercahnts and their merchandise.
                </p>
                <p>
                    Turning into Jalan Tun Tan Cheng Lock, formely known as Heeren Street, the first thing you will notice is how narrow the street is. There is a standing
                    joke that dogs have to wag their tails from side to side. Like all other streets in Chinatown it was originally built to accommodate oxcarts and rickshaws.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>3. BABA NYONYA HERITAGE</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st03.jpg"></p>
                <p>
                    Jalan Tun Tan Cheng Lock was Malacca’s “Millionaire Row”. Here you will find some of the most equisite example of Baba Nyonya houses. Most of these houses
                    display an interesting blend of Dutch and Chinese influences. One house definitely worth stopping at is No 117 with its Dutch architecture, courtyard and
                    silver-painted dome. Of course, you must not miss the Baba Nyonya Heritage Museum, No 48 and 50, for an insight into a way of life that lasted unchanged
                    pretty much until World War II. You will be amazed to discover how the narrow facade belies the airy spaciousness.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>4. CHENG HOON TENG TEMPLE</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st04.jpg"></p>
                <p>
                    On Jalan Hng Lekir, you can see craftsmen constructing traditional Chinese coffins. You migh even be able to witness a typical Chinese funeral parlour.
                    Only in Malacca is the ornate teakwood coffin covered with a gaily coloured canopy before it is conveyed to the cemetery. Turn right into Jalan Hang Jebat
                    or Jonker Street, which known for its many antique and curio shops. You will notice that a number of the shops selling antique furniture belong ti members
                    of the Kutty family.
                </p>
                <p>
                    In other shops you will find all types of antiques- from fragile porcelain and intricate jewellery o Chinese rosewood furniture. There are few shops
                    selling curios, and one, Wah Aik Shoe Maker, still makes and sells little embroidered satin shoes for bound feet.While on this street look out for the
                    oldest building in Malacca, a Dutch trading house built in 1610. Opposite it you will find a house with a very low roof- it is so low, you can touch the
                    tiled roof with no effort at all. In this coffeeshop there is “wantan mee” stall run by two “amah cher”. Distinguished by their single pigtails and their
                    black and white “samfoo”, these “amah’s” had come from China to work as domestics.
                </p>
                <p>
                    As you walk down any street in Chinatown, particularly along Jalan Hang Jebat, you will see itinerant hawkers calling out their wares. This used to be a
                    common sight in any town in Malaysia,but it is not so anymore. Cut through Jalan Hang Lekiu and turn left into Jaln Tokong, or Temple Street, this street
                    has been dubbed the “Street of Harmony” because the houses of prayer of three different religions lie on same side of the street in close proximity,
                    coexisting in harmony for over three centuries.At the end of the street is the Cheng hoon Teng, or the Green Clouds TempleThe colourful stalls clustered
                    around the entrance of the temple sell candles, joss paper and aromatic joss-stick required by devotees.
                </p>
                <p>
                    In the temple you will see devotees performing prayer rites that have been handed down from generation to generation, since the Chinese first settled in
                    Malacca. Their modern dress makes a sharp contrast to the traditional robes worn by the Buddhist monk and nuns who serve the temple.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>5. KAMPONG KLING MOSQUE</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st05.jpg"></p>
                <p>
                    Adjacent to the Cheng Hoon Teng Temple is the newest addition to the street, the buddhist temple of Siang Lin which boasts the large statue of Buddha made
                    of carraras marble in the country.
                </p>
                <p>
                    Opposite Cheng Hoon Teng are shops selling more paraphernalia required for Taoist and Buddhist prayers. At no 11, you will see papier mache dollmakers at
                    work. These papier mache dolls and items represent servants and luxuries, and they are burnt at funerals to ensure the recently departed a good start in
                    the Underworld.
                </p>
                <p>
                    On the other side of Jalan Hang Lekiu is Jalan Tukang Emas, or Goldsmith Street. Here stands the Kampong Kling Mosque. Non-muslims are not permitted to
                    enter the mosque proper but even the courtyard you will be able to appreciate the marvellous architectural touches that make this mosque so unique.
                </p>
                <p>
                    The Kampong Kling Mosque remains central to Malay community life. Centuries of prayer and contemplation have imbued the mosque with a tranquillity and
                    intimacy that is absent from the more modern houses of prayer.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>6. SRI POYYATHA VINAYAGAR TEMPLE</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st06.jpg"></p>
                <p>
                    This ancient Hindu temple is noteworthy for its traditional hallmarks like the minaret, which is decorate with little statue of deities and other
                    mythological figures. Note the striking red figureines of cowns, the sacred animal of Hindus, along the eaves.
                </p>
                <p>
                    The temple is dedicated to the deity Vinayagar who is the favourite among the Hindus because it is believed that he is a remover of obstacles. He is also
                    revered for his filial piety.
                </p>
                <p>
                    As you walk down Jalan Tukang Besi or Blacksmith Street, you can still see blacksmiths moulding their products over charcoal braziers. These skills have
                    remained in the same families for over 300 years. The artisans cling to the belief that their luck resides in the wooden counters in their shops. Thus,
                    although these counters are decades old and quite decrepit, their owners will not have them replaced for fear of losing their luck.
                </p>
                <p>
                    If you have the time, take a side trip along Jalan Hang Kasturi to see the tinsmiths and the bamboo craftsmen. Like the blacksmiths, these are traditional
                    craftsmen. The tinsmith are kept busy producing lanterns, portable altars and other household items, while the bamboo craftsmen still practise the art of
                    making intricate birdcages.
                </p>
                <p>
                    Turning back into Jalan Hang Jebat keep a watch out for Nattukottai moneylenders. These business are still run in the traditional fashion with the
                    moneylenders sitting on the floor behind small tables.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>7. CHRIST CHUCRH</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st07.jpg"></p>
                <p>
                    Back accros the river, you will find yourself on Jalan Kota, or Fort Terrace, which circles Dutch Square and St Paul’s Hill. Your first stop in Dutch
                    Square should be Christ Chuch, the oldest functioning protestant church in the country.
                </p>
                <p>
                    Originally a Dutch Reform Church, it was later consecrated as an Anglican Church. Visitors of the Anglican faith will find it qiute interesting to attend
                    Mass at Christ Church as it is conducted in English,Mandarin and Tamil to cater to the multi-ethnic congregation.
                </p>
                <p>
                    Next to the church is the General Post Office, another example of Dutch architecture. Opposite the post office, along the bank of Malacca River, you will
                    notice a number of public scribes sitting with portable typewriters under shady trees. Once these men were indispensable, helping the illiterate conduct
                    their official and personal business, but now they are a dying breed.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>
                        8. ST PAUL’S HISTORICAL COMPLEX
                    </strong>
                </p>
                <p>
                    Three of the oldest building in Malacca are located in the vicinity of St Paul’s Hill. At the foot of the hill you will find the Stadhuys, on the other
                    side, Porta de Santiago and at the peak, St Paul’s Church.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>STADHUYS</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st09_1.jpg"></p>
                <p>
                    <br/>
                    Walking along Jalan Kota you will arrive at the Stadhuys, once the residence of the Dutch Governor of Malacca and his officers. Built around 1650, it is a
                    testimony to the solidity of Dutch masonry and woodwork, as well as their ingenuity in adopting their native architecture to the humid climate of Malacca,
                    with the inclusion of large windows and wide verandah. Today, the Stadthuys houses the Malacca Ethnographical and Historical Museum.
                </p>
                <p>
                    At the base of the Stadthuys, you will find a plaque with a roll of honor commemorating the members of the Malacca Volunteer Corps who fell during World
                    War II. In front of the Stadthuys in Dutch Square is the Queen Victoria Fountain, erected by the citizen of Malacca to commemorate her Diamond Jubilee. The
                    Tan Beng Swee Clock Tower was erected in 1886 but it is obvious an attempt had been made to blend it in with the older Dutch buildings around it.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>ST PAUL’S CHURCH</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st09_2.jpg"></p>
                <p>
                    <br/>
                    A short climb up St Paul’s Hill will bring you to the ruins of St Paul’s Church. Just imagine, this same path was taken by a saint in the olden days! The
                    church was built by the Portugese in 1521 and St Francis Xavier first preached here in 1545. Aside from his efforts to spread Christianity in the Far East,
                    St Francis was associated with a number of miracles.
                </p>
                <p>
                    Inside the church there is an open, empty tomb where his “incorrupt” body was temporarily placed before it was moved to Basilica of Bom Jesus in Goa,
                    India. The marble statue of St Francis in front of the church was erected to commemorate the fourth centenary of his temporary burial in the church. You
                    will come across an unusual sight of ruins of battlements and cannons in the church. This is a remnant of the Dutch era when the church was converted into
                    an extension of A Famosa. The Dutch had even turned the altar into a cannon embrasure.
                </p>
                <p>
                    As u leave the church, take a moment to look out over the straits of Malacca in the distance. Where once, centuries ago, you would have had a commanding
                    view of junks and galleys jostling for space, now you may see a solitary oil tanker anchored far off in the distance.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>PORTA DE SANTIAGO</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st09_3.jpg"></p>
                <p>
                    <br/>
                    At the foot of St Paul’s Hill is Porta de Santiago, all that remains of A Famosa. Every Malaysian schoolchild will be able to tell you the story of how the
                    fort was built by the portugese in 1512 under the command of Alfonso d’Albuquerque. The date “Anno 1607” inscribed over the portal marks the overthrow of
                    the Portugese by the Ducth. For good measure, the Dutch conquerors also included the crest of the United Dutch East India Company above the date.
                </p>
                <p>
                    In the early 19th century, the British East India Company decided to demolish the fort. Fortunately, Sir Stamford Raffles realised the historical
                    significance of the fort and his timely intervention “saved” Porta de Santiago for posterity.
                </p>
                <p>
                    For many centuries, A Famosa sstood as a symbol of Malacca’s importance as a trading centre between East and West. Today, its ruins remains as a timeless
                    evocation of Malacca’s history-and more.
                </p>
                <p>
                    On Sundays and auspicious days, you can see a parade of Chinese bridal couples happily posing for photographs on Warriors Field in front of Porta de
                    Santiago. They believe that by doing so their marriages will be as enduring as the fort.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>
                        9. MALACCA SULTANATE PALACE
                    </strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st09_4.jpg"></p>
                <p>
                    Proceeding from Porta de Santiago, you will come across another Christian cemetery. Here there are few unoccupied grave-side, which had been dug in the
                    17th century for Ducth subjects.
                    <br/>
                    To the left of the cemetery is the Malacca Sultanate Palace. This replica of the original 15th century palace of Malacca extinct sultanate is based on
                    sketches found in the ancient Sejarah Melayu (Malay Annals). It houses the Malacca Cultural Museum.
                </p>
                <p>
                    Opposite the palace is the Historic City Memorial Garden. There is a strong Islamic influence permeating the garden, in the manner the structures are built
                    and decorate. The focus of this garden is the monument to be quite intriguing as it is topped with a replica of a malay royal headdress, a symbol of the
                    Malaysian citizen’s allegiance to the throne.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>10. PROCLAMATION OF INDEPENDENCE MEMORIAL</strong>
                </p>
                <p><img class="img-responsive" src="images/sports_tourism/st10.jpg"></p>
                <p>
                    Next to the Historic City Memorial Garden is the Proclamation of Independence Memorial. Malaccans still tend to refer to this quaint old building by its
                    original name, the Malacca Club. Its new name is appropriate as the proclamation of Malaya’s independence was made by Malaysia’s first Prome Minister,
                    Tunku Abdul Rahman Putra Al-Haj, at Padang Pahlawan or Warriors Field just across the road.
                </p>
                <p>
                    On his field, you will find the Independence Obelisk. This modest monument marks the spot where the last British Residence of Malacca handed the instrument
                    of independent to the first local Governor of Malacca on August 31,1957. The “M” on all four sides stands for “MERDEKA”, the Malay word for independence,
                </p>
                <p>
                    After this you will head back towards the Tourist Information Centre, from where you started this Heritage Trail into the heart of Old Malacca. This trail
                    has been made possible by a generous grant from the <strong>American Express Foundation.</strong>
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>1. Arts and Crafts</strong>
                </p>
                <p>
                    Malaysian handicrafts are beautiful objects d’art that reflect the diverse and aesthetic values of the communities. As a truly Asia melting pot of
                    cultures, Malaysian craftsmen are imbued with fascinating skills and materials to create their arts and crafts. From the batik sarong and silverware to
                    tribal bamboo blowpipes and gigantic kites, these arts and crafts make perfect gifts and souvenirs for all travellers and their loved ones.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>1.1Kites</strong>
                </p>
                <p>
                    Wau or Kites make great wall hangings, and one of the best places to buy them is in Central Market in Kuala Lumpur. Wau in Malaysia can be traced back to
                    the 15th century when flying kites remains a hobby and passion for many villagers, mostly in the East Coast including Kelantan state. The uniqueness about
                    Malaysian Wau is it is quite large with some range from 2 metres by 3 metres. Most kites are made from colourful waxed paper that has been glued over a
                    light bamboo frame. These are then decorated with flower and animal motifs as well as geometry motifs. Wau Bulan, or Moon Kite, is the most well liked, and
                    it can be seen on Malaysia Airlines logo. There are still kite makers doing their traditional craft in Kota Bharu, with the best ones being located on the
                    main road to Pantai Cahaya Bulan.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>1.2 Pottery</strong>
                </p>
                <p>
                    Pottery comes in all shapes and sizes and is a cultural artefact that reflects the skills of the community The 'Mambong’ pottery in Kelantan, are fashioned
                    from clay and characterized by a red shade. Malay pottery can be categorized into Labu Sayong, Belanga, Buyong and Terenang. Labu Sayong of Perak, the most
                    famous pottery is made of clay and shaped like a gourd and used to contain water. The black coloured Labu Sayong is the most popular. The Belanga, often
                    used to cook curry has a round base and a wide rim. The round shaped base makes heat distributed evenly. Smaller Belanga is brown periuk often used to cook
                    rice. Buyung has a bulbous body and straight neck to contain water. Lastly, Terenang is angular-shaped pottery popular in Pahang and Terengganu helps carry
                    water. It resembles brassware, and has a concave neck and a convex-shaped body.
                </p>
                <p>&nbsp;</p>
                <p>
                    <strong>1.3 Weaving</strong>
                </p>
                <p>
                    Weaving is a popular traditional craft among village women. In the past, a maiden would be considered incomplete if she did not know how to weave. Pandan
                    and mengkuang leave weaving to make mats, placemats, bags, hats and tudung saji (conical shape food covering) is popular. The process involves leaves that
                    have been stripped of thorns are split into strands, soaked, dried and dyed and then woven according to the required designs. Other kinds of weaving are
                    bamboo and rattan to make baskets, bags, pencil holders and even furniture.
                </p>
                <p>
                    Another interesting souvenir is “Ikat” which stands for cord, thread, knot and part of the weaving process.It is a dyeing technique used to pattern
                    textiles. In East Malaysia, woven Ikat cotton cloth featuring ethnic patterns is a common sight in local markets. Cotton ikat is produced on wooden looms
                    and called Pua Kumbu, and it is still woven by villagers. The design can be simple and symbolic and it can also be vibrant with crocodiles, hornbills and
                    lizards as part of the design element. Today, there are many examples of these textiles in the souvenir shops in Sarawak, and the cloth makes great wall
                    hangings, throw-overs, or even blankets.
                </p>
