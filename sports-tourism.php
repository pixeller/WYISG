                    silver or golden threads. Real gold was once used in the past which makes songket become an expensive cloth worn by the well-to-do or royalty. Songket
                    weaving is another old craft used to produce intricate fabric that is still popular for wedding events and official ceremonies; it is known as the “king of
                    local textiles.” Like batik, the best songket is found in the East Coast states of Kelantan and Terengganu.
