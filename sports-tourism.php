                    animal designs that capture the essence of Malaysian elements. In Sarawak, local craftsmen are mostly natives while in Peninsular Malaysia, the Mahmeri and
                    Jan Hut aborigines are noted for their carvings of mythical creatures related to their folklores. Wood crafts also include shutters, friezes and panels of
                    old Malay houses and these are sold as antique items in some stores. You can also find wood crafts of miniature replicas of Petronas Twin Towers,
                    traditional Malay houses and other Malaysian elements. There are also hornbills, figurines and ceremonial masks carved from timber and found aplenty in
