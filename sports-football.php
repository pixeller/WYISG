                <h4> HISTORY </h4>
                <p> The history of American football can be traced to early versions of rugby football and association football. Both games have their origin in varieties of football played in Britain in the mid-19th century, in which a football is kicked at a goal or run over a line. </p>
                <p> American football resulted from several major divergences from rugby, most notably the rule changes instituted by Walter Camp, a Yale and Hopkins School graduate considered to be the "Father of American Football". Among these important changes were the introduction of the line of scrimmage and of down-and-distance rules.[1][2][3] In the late 19th and early 20th centuries, gameplay developments by college coaches such as Eddie Cochems, Amos Alonzo Stagg, Knute Rockne, and Glenn "Pop" Warner helped take advantage of the newly introduced forward pass. The popularity of college football grew as it became the dominant version of the sport in the United States for the first half of the 20th century. Bowl games, a college football tradition, attracted a national audience for college teams. Boosted by fierce rivalries, college football still holds widespread appeal in the US. </p>
                <p> The origin of professional football can be traced back to 1892, with William "Pudge" Heffelfinger's $500 contract to play in a game for the Allegheny Athletic Association against the Pittsburgh Athletic Club. In 1920 the American Professional Football Association was formed. This league changed its name to the National Football League (NFL) two years later, and eventually became the major league of American football. Primarily a sport of Midwestern industrial towns in the United States, professional football eventually became a national phenomenon. Football's increasing popularity is usually traced to the 1958 NFL Championship Game, a contest that has been dubbed the "Greatest Game Ever Played". A rival league to the NFL, the American Football League (AFL), began play in 1960; the pressure it put on the senior league led to a merger between the two leagues and the creation of the Super Bowl, which has become the most watched television event in the United States on an annual basis <p/>
                
