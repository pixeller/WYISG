                <h4> DESCRIPTION </h4>
                <p> Football is a dynamic sport that demands peak physical performance and exceptional mental acuity on the part of its players. It is a game that is simple in essence but complex in application. At a fundamental level, football is basically a version of Capture the Flag; get to the opponent’s end of the field to win the drive and collect points. However, the game provides multiple layers of subtle complexity that contains both a strategic and a tactical dimension. </p>
                <h5> Gridiron Basics </h5>
                <p> Football is played on a rectangular field that is 120 yards in length and 53.3 yards in width. The area of the field designated for play where the ball may be spotted is 100 yards in length. There is a scoring end zone on each end of the field that is 10 yards long. Eleven players for each team take the field on any given play. The field also includes a set of upright goalposts at the ends of the field for scoring points after touchdown and field goals. The dimensions of the goalposts vary by league, but for college and professional football, the bottom crossbar must be 10 feet above the ground, the upright posts must extend 30 feet above the crossbar, and the two posts must be 18 1/2 feet apart. The game itself is most easily categorized into three sections – the offense, defense and kicking segments. </p>








