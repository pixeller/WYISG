                <h4> HISTORY </h4>
                <p> The name Taekwondo is derived from the Korean word "Tae" meaning foot, "Kwon" meaning fist and "Do" meaning way of. So, literally Taekwondo means "the way of the foot and fist". The name Taekwondo, however, has only been used since 1955 while the arts' roots began 2,300 years ago in Korea. Known as a martial art and way of life, the evolution of Taekwondo was a direct result of the happenings in Korea long ago, and knowledge of the history is an important step in understanding Taekwondo. </p>
                <p> Korean history began when in 2333 B.C. the legendary national founder, Tangun, founded "Old Korea" at Asadal. As in the histories of other nations, communal life was gradually transformed into tribal communities, and then tribal leagues and finally took the form of a state. Although no written history of the fighting systems of this time remains it is known that the people of this time were hunters and had some means of protection as well as livelihood. </p>
                <p> During the 6th century A.D. what we now call the Korean peninsula was divided into three kingdoms; Koguryo, Paekje and Silla. </p>
                <p> "Hwarangdo" in Shilla and "Chouisonin" in Koguryo, which both adopted martial art training as one of the important subjects of learning. A known martial art book of the days, called "Muyedobo-Tongji" wrote "Taekwondo is the basis of martial art, enabling one to build strength by using the hand and foot freely and training arms and legs as well as the body to adaptable to any critical situations," which means Taekwondo was already prevalent in that age. Thus, it can be easily assumed that Taekwondo was originated from the days of tribal communities on the Korean peninsula. </p>
                <p> Shilla kingdom founded in B.C. 57 on the southeastern part of Korea, and Koguryo founded in B.C. 37 on the northern part of Korea along Yalu river, both made great efforts to raise their youngsters into strong warriors called "hwarang" and "sunbae" respectively, certainly with Taekwondo as one of the principal subjects of physical training. </p>

