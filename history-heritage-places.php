                    <b> Malacca Sultanate Palace Museum </b>
                        <p> The Malacca Sultanate Palace Museum is a museum located in Malacca, Malaysia. The building is a modern reconstruction of the palace of the sultans of Malacca. It showcases the history of the region. The museum building was constructed in 1984 and became a cultural museum. The museum was officially opened on 17 July 1986 by Prime Minister Mahathir Mohamad. </p>
                        <p> The Malacca Sultanate Palace is actually a replica of the structure which was built by the already extinct sultanate of Malacca. The sultanate of Malacca was the ruler of the city before the arrival of the Portuguese in the 1500s. The plan of the palace was based on the Malay Annals' account of Sultan Mansur Shah's palace. Sultan Mansur Shah was the nobleman who ruled Malacca from 1456 to 1477. The Malacca Sultanate Palace is one of its kind in Malaysia as it offers a rare glimpse of the ancient Malay kingdom which once flourished in Malacca. </p>
                        <p> Today, the palace is the home of the Cultural Museum which celebrates the Malay side of the Malacca's history. The museum showcases more than 1300 items of Malacca's past. There items include photographs, weaponry, drawings, musical instruments and gifts from foreign emissaries. All these items are displayed in eight different chambers and in three galleries on the three floors of the palace. Admission into the palace is RM2 per adult and RM0.50 per child. It is closed on Mondays and opens on Tuesdays to Sundays from 9am to 6pm. Since the palace is at the foot of St Paul's Hill, do not miss the chance to visit the St Paul's Church, which is at the top of the St Paul's Hill. </p>
                        <p> Along the way up the hill, you will also pass by some museums. They are the Architecture Museum Malacca and the Stamp Museum Malacca. Also close by is the Islamic Museum of Malacca where you can view displays of how Islam came to Malacca and spread to the entire country. There certainly are many places of interest to visit in Malacca. A day trip to this wonderful place is not adequate. Do consider staying at any hotels in Malacca as there are hotels of all types which will cater to your every need. </p>

