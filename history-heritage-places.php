                     <b> A Famosa </b>
                        <p> A Famosa is a Portuguese fortress located in Malacca, Malaysia. It is among the oldest surviving European architectural remains in South East Asia. The <i> Porta de Santiago </i>, a small gate house, is the only part of the fortress which still remains today.  </p>
                        <p> In 1511, a Portuguese fleet arrived under the command of Afonso de Albuquerque. His forces attacked and defeated the armies of the Malacca Sultanate. Moving quickly to consolidate his gains, Albuquerque had the fortress built around a natural hill near the sea. </p> 
                        <p> Albuquerque believed that Malacca would become an important port linking Portugal to the Spice Route in China. At this time other Portuguese were establishing outposts in such places as Macau, China and Goa, India to create a string of friendly ports for ships heading to Ming China and returning home to Portugal. </p>
                        <p> The fortress once consisted of long ramparts and four major towers. One was a four-story keep, while the others held an ammunition storage room, the residence of the captain, and an officers' quarters. Most of the village clustered in town houses inside the fortress walls. As Malacca's population expanded it outgrew the original fort and extensions were added around 1586. </p>
                        <p> The fort changed hands in 1641 when the Dutch drove the Portuguese out of Malacca.[1] The Dutch renovated the gate in 1670, which explains the logo "ANNO 1670" inscribed on the gate's arch. Above the arch is a bas-relief logo of the Dutch East India Company. </p>
                        <p> The fortress changed hands again in the late 18th century when the Dutch handed it over to the British to prevent it from falling into the hands of Napoleon's expansionist France. The English were wary of maintaining the fortification and ordered its destruction in 1806. The fort was almost totally demolished but for the timely intervention of Sir Stamford Raffles, the founder of modern Singapore, who happened to visit Malacca in 1810. Because of his passion for history, this small gate was spared from destruction. </p>
                        <p> In late November 2006, a structure part of the fort, believed to be the Middelsburgh Bastion was accidentally uncovered during the construction of 110 meter revolving tower in Malacca Town. </p>

