                    <b> Taming Sari Tower </b>
                        <p> The Taming Sari Tower is a revolving gyro tower in Malacca City, Malacca, Malaysia. Taming Sari Tower offically opened for business on the 18th of April 2008, it is the first and only gyro tower in Malaysia so far. Measuring 110 metres in height, its revolving structure offers a 360-degree panoramic view of Malacca town and beyond.  </p>
                        <p> Located in the popular district of Bandar Hilir on Jalan Merdeka, only 3 minutes’ walk from Mahkota Parade Shopping Complex and Dataran Pahlawan Megamall, the tower is named after the Taming Sari keris, a mythical weapon said to possess mystical powers belonging to the legendary Malay warrior, Hang Tuah. Even the structure’s design follows the shape of the keris, with the peak of the tower resembling the its hilt. </p>
                        <p> Built using advanced Swiss technology, the tower’s structure is reportedly tough enough to withstand earthquakes measuring up to 10 on the Richter scale. Able to accommodate 80 people per viewing session, which lasts about 7 minutes, the glass cabin hypnotically revolves as it rises from the ground to the top of the tower. </p>
                        <p> Once it reaches the top, a breath-taking, sprawling view of Malacca as far as the eye can see, up to the Straits of Malacca, is clearly visible, especially in good weather, with the Melaka River running through the grand panorama below. </p>
                        <p> In addition to the opportunity to get a bird’s-eye view of Malacca, the attraction is also fitted with 5 commercial outlets consisting of a café, a restaurant and souvenir shops. </p>

