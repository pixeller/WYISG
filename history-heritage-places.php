                    <b> Melaka Maritime Museum </b>
                        <p> Melaka Maritime Museum is a replica of the Flor de la Mar, a Portuguese ship said to have been carrying loot plundered from Malacca when it sank off the coast of Malacca on its way back to Portugal.  </p>
                        <p> Opened to the public in 1994, this unique museum is an enlightening trip back into Malacca’s past. The 34m-high, 36m-long and eight-metre-wide structure is located at Quayside Road. Set 10 minutes away from the Dutch Square, the Melaka Maritime Museum is divided into the different eras that Malacca has gone through, from the Melaka Sultanate period, to Portuguese, Dutch and British rule. </p>
                        <p> The Melaka Maritime Museum aims to highlight Malacca’s importance back in the day as ‘a regional and international business centre’. Displays include artefacts and documents from the Malacca Sultanate, Portuguese, Dutch and British administration era that reveal why political control of Malacca was essential to maritime dominance of the region. There are also paintings which displays how the Straits of Malacca was a strategic location for traders from both the east and the west stop by at Malacca and conduct their businesses while waiting for the monsoon winds to change direction. The museum has myriads of exhibits of articles such as porcelain, silk, textile and spices used by the seamen of the yesteryears and even come sunken treasures hauled from Diana, another shipwreck. Other than exhibits on the history of the Malaccan Maritime, the museum also showcases the different eras that Malacca has gone through starting from the Malacca Sultanate era to the Portuguese era, Dutch era and ending with the British era. </p>

