                    <b> Jonker Walk </b>
                        <p> The Jonker Walk is the Chinatown street of Malacca, Malaysia located along Jalan Hang Jebat. </p>
                        <p> Jalan Hang Jebat, or Jonker Walk is it commonly known, is a street in historical Chinatown of Melaka starting just across the river from the Dutch Square. Jonker Walk is flanked on both sides of the road with heritage houses dating back up to the 17th century. At that time, rich Baba Nonyas (also known as Perankans, descendents of 15th - 16th century Chinese immigrants marrying local Malay women) lived and did their business here. </p>
                        <p> During the Dutch Malacca, servants and subordinates of Dutch masters used to live at the nearby Heeren Street. However, as the Dutch left, it became noblemen's street. Rich Peranakans started to live and did business within the street area, giving the street a deep-rooted ethnic and cultural flavour. The road starts from across Malacca River near the Stadthuys. The road is filled with historical houses along its left and right sides dating back to 17th century. It also has shops selling antiques, textiles, foods, handicrafts and souvenirs </p>
                        <p> Today, the street houses many shops selling antiques, textiles, handicrafts and souvenirs. On the weekends, starting Friday night at 6pm, the road is closed off for traffic and turns in a very crowded night market. </p>

