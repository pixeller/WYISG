                    <b> Baba Nyonya Heritage Museum </b>
                        <p> The Baba & Nyonya House Museum, also known as the Baba Nyonya Heritage Museum, is a museum in Malacca, Malaysia. It showcases the local history of ethnic Chinese-Malays called Baba-Nyonya or Peranakan in Malacca. The museum was established in 1986 by Chan Kim Lay, the fourth generation of his family to reside in the large house built by his great-grandfather in 1896 in Jalan Tun Tan Cheng Lock, a street also known as "Millionaire's Row" for its luxurious houses. The museum is known for its elaborate woodwork, furniture, and porcelain.  </p>
                        <p> The house is a combination of three terrace lots that were acquired by the Chan family in 1861. Four generations lived in the house before it was opened as a museum in March 1985. The name of the museum is derived from the term <i> Baba </i>, an honorific manner of addressing a Straits-born gentleman and <i> Nyonya </i> which addresses a Straits-born lady. </p>
                        <p> We welcome you on a journey into late 19th-century and early 20th-century Malaya.  Meander through the home of Baba Chan Cheng Siew (1865-1919) whose eclectic taste offers a glimpse into the richness of the culture and the opulence that was fashionable in many pre-World War II Peranakan homes. </p>
                        <p> Today the museum is still managed by the family. The vision is to revive Malaysia’s Peranakan identity and heritage. By preserving this house gives honour to the Peranakan spirit, keeping it alive for many generations. </p>

