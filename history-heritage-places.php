                <h4> Heritage Places (Melaka Tengah) </h4>

                    <b> Malacca Straits Mosque </b>
                        <p> The straits of Melaka is the Longest and the busiest strait in the world. The important of the strait is equivalent to Suez Canal or the Panama Canal. The strait is named after the State of Melaka because of the Long History of Melaka Sultanate Empire 600 years ago. Stretching about 500mile between Malaysian Peninsular (West) and the Island of Sumatra, Indonesia. </p>
                        <p> State Government of Melaka has build a Mosque at the coast of Selat Melaka in Bandar Hilir. Located at a Man Made Island named Pulau Melaka (Island of Melaka) is the one and only Mosque build at the coast of the Strait and at the water level of the sea. It will look like floating if the water level is high / tide happen. The Mosque or known as ‘Masjid Selat’, costing about MYR10 Million. The Opening Ceremony was done in 24th November 2006 by the Supreme Ruler of Malaysia (King) – Yang Dipertuan Agong Tuanku Syed Sirajuddin Syed Putra Jamalullail. </p>
                        <p> Visitors and Tourist also can buy a Certificate for staying at the Longest and Busiest Strait in the World. We are allowed to go near the water but no fishing allowed. The surrounding of the Strait Mosque are still under development and you will see many unoccupied building and shop lot. So the attraction here are just the Strait Mosque build in a Man Made Island and the Strait of Melaka itself. </p>

