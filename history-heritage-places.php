                    <b> Stadthuys </b>
                        <p> The Stadthuys (an old Dutch spelling, meaning city hall), also known as the Red Square, is a historical structure situated in the heart of Malacca Town, the administrative capital of the state of Malacca, Malaysia. It was built by the Dutch occupants in 1650 as the office of the Dutch Governor and Deputy Governor. </p>
                        <p> When Malacca was handed over to the British in the 19th century, the Malacca Free School was opened in the vicinity of the Stadthuys on 7 December 1826, by missionaries residing in the state, in response to a letter dated 19 April 1825, signed by a J. Humprey, J. W. Overee and A. W. Baumgarten, which called for an English institutional education to be built in Malacca. The school which the British provided free education to residents was eventually renamed Malacca High School in 1871 upon a takeover by the British government, and moved out to its present site at Chan Koon Cheng Road in 1931. </p>
                        <p> Situated at Laksamana Road, beside the Christ Church, the supposed oldest remaining Dutch historical building in the Orient, is now the home of a Museum of History and Ethnography. Among the displays in the museum are traditional costumes and artefacts throughout the history of Malacca, which makes it Malacca's premier museum. </p>

