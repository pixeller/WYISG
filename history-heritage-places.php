                    <b> Cheng Hoon Teng Temple </b>
                        <p> Is a Chinese temple practicing the Three Doctrinal Systems of Taoism, Confucianism, and Buddhism located at Malacca town, Malaysia. It is the oldest functioning temple in Malaysia. It also known as The Green Clouds Temple. </p>
                        <p> Built in 1645 by Kapitan Lee Wei King with building materials imported from China, Cheng Hoon Teng served as the main place of worship for the local Hoklo (Hokkien) community. The main hall was built by Kapitan Chan Ki Lock in 1704 and was rebuilt in 1801 by Kapitan China Chua Su Cheong, who contributed to the aesthetic and magnificent structural additions of the building. </p>
                        <p> In 1962, then Abbot Seck Kim Seng ordained Houn Jiyu-Kennett, a Zen nun from England and the future founder of the Order of Buddhist Contemplatives, at this temple. </p>
                        <p> The richly decorated Cheng Hoon Teng temple covers an area of 4,600 m2. </p>
                        <p> Featuring a magnificent main gate along Jalan Tokong, the Cheng Hoon Teng temple consists of a complex of several prayer halls, with a large main prayer hall dedicated to the goddess of mercy, Kuan Yin. Additional smaller prayer quarters were added later. One of these is dedicated to the Buddhist gods of wealth, longevity and propagation, while another houses ancestral tablets. </p>
                        <p> One of the most dramatic features of Cheng Hoon Teng temple is the seven-metre red flag-pole facing the left wing of the main prayer hall, which houses the remains of two of the three Kapitans who contributed to the construction of the temple. Across the road is a traditional opera theatre, which forms a part of the Cheng Hoon Teng temple complex. </p>
                        <p> The building conforms to the principles of feng shui. The complex is laid out to ensure a view of the river and high ground on either side. </p>
                        <p> In 2003, Cheng Hoon Teng was awarded a UNESCO award for outstanding architectural restoration </p>
                            
