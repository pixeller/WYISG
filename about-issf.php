                <h2>ABOUT ISLAMIC SOLIDARITY SPORTS FEDERATION (ISSF)</h2>
                <p>At the 3rd Islamic Summit Conference held in Mecca and Al Taif (1981), the leaders of Islamic world adopted a decision to establish the Islamic Solidarity Sports Federation (ISSF) to serve the needs of the OIC member countries in all aspects of sports activities. In 1985, under the initiative of HRH the late Prince Faisal Fahd Abdulaziz, President of Saudi Arabia’s General Presidency of Youth Welfare, the OIC has invited the NOCs and Youth Sports governmental organizations in Muslim countries to attend the constituent assembly for the foundation of the ISSF in the Kingdom Of Saudi Arabia. Representatives of 34 Muslim countries attended this assembly and ratified the ISSF statue. Under the guidance of the Government of the Kingdom of Saudi Arabia, the late Prince Faisal bin Fahd announced the allocation of the ISSF headquarters to be at the Olympic complex in Riyadh city. He also announced the allocation of an annual subsidy from the Kingdom of Saudi Arabia for the administration of the ISSF. There are currently 57 OIC members in the union. </p>
                <p>Objectives & task of the ISSF:</p>
                <ul>
                	<li>To supervise the organization of a multi-sport event be held once every four years for member countries. This event shall be called the Islamic Solidarity Games.</li>
                    <li>To supervise the organization of tournaments and championships in all sports</li>
                    <li>To encourage the exchange of bilateral visits between sports teams of Member countries.To enter into cooperation agreements with sports organizations at international, continental and regional levels</li>
                    <li>To train and develop sports cadres in Member states through the exchange of technical expertise and the organization of training courses in various fields of sports.</li>
                    <li>To hold conferences, symposia, exhibitions and museums, to supervise studies and researches and to disseminate publications serving the I.S.S.F goals</li>
                </ul>

