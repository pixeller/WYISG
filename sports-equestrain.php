                <h4> HISTORY </h4>
                <p> The history of Equestrian sport dates back over 2,000 years, to when the Greeks introduced Dressage training to prepare their horses for war. </p>
                <p> It continued to develop as a military exercise through the Middle Ages, with the Three Day Event – which includes Dressage, Cross Country and Show Jumping tests – designed to reflect the range of challenges horses faced in the army. In its modern form, Equestrian owes much to its inclusion in the Olympic Games, which led to the creation of the Fédération Equestre Internationale (FEI) in 1921. </p>
                <p> Equestrian is the only Olympic sport in which men and women compete against each other on equal terms. It is also the only one in which humans and animals compete together. In each of the three Equestrian events – Dressage, Jumping and Eventing – the aim is to create a harmonious partnership between horse and rider. Equestrian sport featured on the Olympic programme for the first time at the Games of the II Olympiad in Paris in 1900. It was not staged at the next two editions of the Games, but returned in 1912. Since then, the sport has always been on the programme. Until 1948, only men could compete in the events, as the riders had to be officers. This restriction was lifted in 1951 and, since the Games of the XV Olympiad in Helsinki in 1952, women have competed with men in mixed events. They competed first in dressage, then gradually in the other equestrian events. </p>

