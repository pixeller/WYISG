                <h4> HISTORY </h4>

                <h5> Oldest game </h5>
                <p> Hockey is the oldest known ball and stick game: records exist of it being played in Persia in 2000 BC. The name “hockey” probably derives from the French “hocquet”, or shepherd’s crook, and refers to the crooked stick which is used to hit a small ball. </p>


                <h5> British development </h5>
                <p> The first team was the Blackheath Hockey Club, dating back to 1861. The Amateur Hockey Association was then formed in London in 1886, before workers and the military carried the game to the four corners of the British Empire. It became an Olympic sport in 1908. </p>


                <h5> Smoother surfaces </h5>
                <p> Until the 1970s, the game at international level was mainly played on natural grass, but the heavy turf made the game quite slow. When plastic turf was first used, the game suddenly became quicker and more exciting, giving birth to the modern game. </p>


                <h5> Dominant nations </h5>
                <p> Most of the dominant nations in the sport are, or were, members of the British Empire. This includes India, Pakistan, Australia, New Zealand and, of course, England. Other nations have come to the forefront in more recent times to make the game a truly worldwide sport. </p>


                <h5> Olympic history </h5>
                <p> After a first appearance at the 1908 Games in London, hockey became a firm fixture on the Olympic programme as from the Antwerp Games in 1920. Women made their entrance in this sport in 1980 at the Moscow Games. Since the 2000 Games in Sydney, men have competed in a 12-team tournament and women in a 10-team one. </p>
                <p> In its 100 years of Olympic existence, hockey has been essentially dominated by one country. The Indian men’s team, with six consecutive titles between 1928 and 1956, was unbeaten in 30 consecutive matches, and scored 197 goals, giving away only eight. </p>



                <p>&nbsp;</p>

                <h4> DESCRIPTION </h4>
                <p> The International Ice Hockey Federation (IIHF) was founded in 1908. Ice hockey made its Olympic debut at the 1924 Chamonix Winter Games. </p>

                <h5> Players </h5>
                <p> A team must not have more than six players on the ice while play is in progress. Typically, these players are one goalkeeper, two defenders, two wings and one centre. However, fewer players can be on the ice as a result of penalties, and a goalkeeper can be replaced for another skater at any time at the team’s own risk. </p>

                <h5> Events and differences </h5>
                <p> Women currently compete in an eight-team tournament, whereas men compete in a 12-team tournament. The chief difference between women's and men's ice hockey is that body checking is not allowed in women's ice hockey. </p>

                <h5> Competition procedure </h5>
                <p> A regular game consists of three 20-minute periods, with a 15-minute intermission after the first and second periods. Teams change ends for each period. If a tie occurs in a medal-round game, a five-minute sudden-victory overtime period is played. In the gold medal game, a 20-minute sudden-victory period is played subsequent to another 15-minute intermission. </p>

                <h5> Penalty shoot-out </h5>
                <p> In the case of a tie after any sudden-victory period, a game-winning penalty shoot competition takes place to determine the winner. </p>




