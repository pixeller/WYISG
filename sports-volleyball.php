                <h4> DESCRIPTION </h4>
                <p> Volleyball is a popular competitive sport that can be played by players of all ages, indoors or outside. It originated in the United States in 1895 as a blend of basketball, baseball, tennis, and handball.  </p>
                <p> It can be played in pairs or teams. The spiking, passing, blocking and setting of volleyball uses almost every major muscle group of the body, and requires lightning fast reactions and superior flexibility. A casual game of volleyball may involve two teams of three or more players in an indoor or outdoor volleyball court setting. </p>
                <p> The goal of volleyball is to ground the ball in the opponent's court. A player on one of the teams begins a rally by serving the ball (tossing or releasing it and then hitting it with a hand or arm), from behind the back boundary line of the court, over the net, and into the receiving team's court. The receiving team must not let the ball be grounded within their court. They may touch the ball as many as three times. </p>
                <p> Typically, the first two touches are to set up for an attack or spike, an attempt to direct the ball back over the net in such a way that the serving team is unable to prevent it from being grounded in their court. Volleyball exercises may involve bumping the ball, running, jogging, and serving the ball. It entails superior fitness levels as team members pass the ball to each other and then spike it against the opposing team. </p>


               
