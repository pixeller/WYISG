                <h4> HISTORY </h4>
                <p>Archery is a sport that dates back years and years before the modern day version that we have today with all sorts of high-tech equipment and different techniques of shooting and competing with other archers. To give you an idea of just how old archery really is, in approx. 2800 BC, the first composite bow was produced by the Egyptians. It was made from wood, tipped with animal horn and held together with animal sinew and glue. Unstrung, it resembled a "C" shape and would have required 2 people to string it. The bowstring was made from "catgut" (sheep intestines). The arrows used were extremely light, could be shot 400 yards using the composite bow and would easily penetrate the armor of that time period. The Egyptians used archers on the back of light chariots who were highly trained and skilled and could easily outflank an enemy army with devastating effect.</p>
                <p>archersThe bow was retained as a hunting weapon, and archery continued to be practiced as a sport in England by both royalty and the general public. The earliest English archery societies dated from the 16th and 17th centuries. The oldest continuously held archery tournament still extant, known as the Ancient Scorton Arrow, was founded in Yorkshire in 1673; and about 1790 the Royal Toxophilite (Greek toxon, "bow"; philos, "loving") Society was formed to advance the sport. The Prince of Wales, afterward George IV, became the patron of this Society and set the prince's lengths of 100 yards (91 m), 80 yards (73 m), and 60 yards (55 m); these distances are still used in the British men's championship York Round (six dozen, four dozen, and two dozen arrows shot at each of the three distances).</p>
                

