                <h4> HISTORY AND DESCRIPTION OF ARCHERY </h4>
                <p>Archery by definition is the art, practice, or skill of shooting with a bow and arrow</p>
                <p>The bow and arrow was invented around 25,000-30,000 years ago during the Upper Paleolithic Period. This highly advanced tool at that time was instrumental in hunting and warfare. The use of bows and arrows has been discovered all over the world either through ancient art or archaelogical findings. Many battles throughout the history of mankind were won with the use of this accurate shooting instrument.</p>
                <p>Archery has been a major tool for hunting and warfare throughout the development of mankind up until the advent of firearms. Although still used by certain African tribes during warfare, archery is more commonly a target shooting past time or big game hunting option.</p>
                <p>Quick links to sections on this page</p>
                <p>The Long Bow,The Recurve Bow,The Crossbow,The Compound Bow,Archery Arrows</p>
                <p>Olympic Archery,Competition Archery,Bow Hunting,Archery Bows</p>
                <p>The most commonly used bows today are the compound bow, recurve bow, long bow, and the cross bow.<p/>

                <p>&nbsp;</p>
                <h5> The Compound Bow </h5>
                <p>&nbsp;</p>
                <h5> archery compound bow </h5>
                <p> First developed and patented by american inventor Holless Allen in 1966, the compound bow revolutionized the sport of archery. The much more energy efficient compound bow uses a system of stiff limbs, a bow string, cables, riser and cams (pulleys) in order to store a large amount of energy into the bow’s limbs while relieving the archer from peak draw weight at full draw. Modern compound bows may have a draw weight ranging from 50-100 lbs but when at full draw, due to the mechanical design of the bow, the archer is only holding 20-25% of the draw weight at full draw. When drawing a bow that requires 70 pounds of force, the archer draws back reaching peak draw weight well before full draw. While at this moment of the draw, the bow’s limbs are under their greatest ‘load’ or bend. After peak weight is reached, the compound bow’s cam system ‘rolls over’ already having stored maximum bend or load in the limbs, the draw weight is lessened, and the archer is actually only ‘holding’ 14-20 pounds. During the shot, the cams release the stored energy and the arrow is delivered with great kinetic energy. Modern compound bows can reach speeds exceeding 365 feet per second. Draw lengths are adjustable on a compound bow by changing modules, cams, or by adjusting setting on the cams.</p>
                <p>&nbsp;</p>
                <h5> The Longbow </h5>
                <p> The longbow was the first bow invented. Hunter gatherers typically made this bow out of yew wood, but longbows have also been made of osage orange, hickory, oak, beech, birch, ash, elm, and bamboo. Longbows, throughout the years have been between the lengths of 4 and 7 feet, with an average length between 6-6.5 feet. Modern longbows have a draw weight of around 50-60 lbs while longbows of the past reached draw weights of 150+ lbs. The effective rang of longbows has been known to be around 200 or more yards. During medieval warfare, thousands of archers would fill the sky with potentially lethal arrows from the edge of their weapons range. The historically significant longbow is still available for purchase and is still used today in traditional target archery and hunting. </p>
                <p>&nbsp;</p>

                <h5> The Recurve Bow </h5>
                <p> The recurve bow is a bow, when not strung with a bow string, that has tips that curve forward of the archery. When the archer strings a recurve bow, the forward curved limbs are bent back towards the archery to enable the archer to string the bow string on the limb tips. The strung recurve’s tips are now vertical in lie and the bows limbs are now preloaded with stored energy allowing the bow to shoot a much faster arrow than its predecessor the longbow. The recurve bow’s energy storing limbs allows the bow to be shorter in length than the longbow thus becoming more convenient to use in tight quarters situations such as in thick cover or on horseback. The recurve bow is still a popular bow choice for use in traditional target archery and hunting. </p>
                <p>&nbsp;</p>
                <h5> The Crossbow </h5>
                <p> The original crossbows consisted of a bow with recurved limbs which was mounted on a stock similar to a gun stock. The limbs were fashioned to have a stronger resistance to bending therefore required much more force to draw back when compared to longbows and recurve bows. Due to the high draw weight, various mechanisms were created to aid the archer in drawing the crossbow to full draw. When the crossbow’s string is pulled back to full draw, a locking mechanism is engaged, locking the bow’s string, keeping the crossbow at full draw until the archer triggers the shot. Modern crossbows work under the same mechanics but with a compound style bow mounted on the stock. The crossbow is currently a very popular choice of avid target archers and bow hunters. </p>
                <p>&nbsp;</p>
                <h5> Archery Arrows </h5>
                <p> Archery arrows are made from a variety of materials which include wood, aluminum, carbon fiber and fiberglass. Wooden arrows were the most commonly used until 1939 when James Easton made the first arrow shaft out of aluminum. Aluminum offered a very straight rigid arrow which became the most popular arrow all the way into the 1990’s. In 1983, the Easton company developed the first carbon arrow. The carbon arrow, which is considerably lighter than the aluminum and wooden arrow, offered excellent straightness and the ability to flex and return to its original state without losing its original shape. These benefits of the carbon arrow are what makes it the most popular arrow on the market today. </p>
                <p> The notched part of the arrow which attaches to the bow string is called the nock. In earlier times a groove was cut into the wooden arrows to serve this function. Today’s modern nocks are made of a hard plastic and insert into the end of the hollow arrow. </p>
                <p> Near the rear of the arrow, just forward of the nock, are the fletchings. Fletchings, which are fin shaped materials, slice through the air with little resistance during a perfect arrow flight. When the bow is out of tune and the tail of the arrow is not on the same path as the head of the arrow, the fletchings react with the air’s friction and stabilize the arrow’s trajectory. The fletchings ensure the tail and the head of the arrow follow the same path leading to greater accuracy. </p>
                <p>&nbsp;</p>
                <h5> Olympic Archery </h5>
                <p> Archery has been a sport in 15 Summer Olympics with the earliest year being 1900. Archery has became a mainstay event in recent years being included in the last 11 consecutive Summer Olympics. </p>
                <p>In today’s Olympic archery contests, a highly skilled archer with outstanding archery form has to make shots at a 1.22 meter ( 4 feet ) wide target 70 meters ( almost 230 feet ) away with a 12.2 centimeter ( 4.8 inch ) wide bullseye approximately 1.3 meters ( 4 feet, 3 inches ) off the ground. The target has 12.2 centimeter (4.8 inch ) wide concentric rings which are used for scoring. </p>
                <p> The scoring starts out at the bullseye ( gold inner) at 10 points descending one point for each ring outward until the final white outer ring which is worth 1 point. The color scheme of an Olympic target from the bullseye outward is: gold inner, gold outer, red inner, red outer, blue inner, blue outer, black inner, black outer, white inner, white outer. </p>
                <p> Archers may only use recurve bows equipped with one pin sight having draw weights around 48 lbs for men and 38 lbs for women. The archers arrows can range from 5.5 millimeters ( 0.217 inches ) to 9.3 millimeters ( 0.366 inches ) wide and must have distinctive markings to identify their shooter. Seeding rounds consist of 72 shots per shooter which from the results will rank them for the 12 shot head to head elimination rounds. There are 4 gold medals which can be won for men’s and women’s individual and team competitions. </p>


