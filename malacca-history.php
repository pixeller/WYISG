                <h4>Dutch Invasion </h4>
                <p>The Jesuit missionary Francis Xavier spent several months in Malacca in 1545, 1546, and 1549. The Dutch ruled Malacca from 1641 to 1798. Malacca was ceded to the British in the Anglo-Dutch Treaty of 1824 in exchange for Bencoolen on Sumatra with Dutch. From 1826 to 1946 Malacca was under the rule of the British, first by the British East India Company and then as a Crown Colony. It formed part of the Straits Settlements, together with Singapore and Penang. After the dissolution of this crown colony, Malacca and Penang became part of the Malayan Union in 1946, which later became the Federation of Malaya in 1948 and eventually Malaysia in 1963. </p>
