                <h4>Portuguese trading expedition </h4>
                <p>By the 15th century, Europe had developed an insatiable appetite for spices. At that time, spice trade was virtually monopolised by the Venetian merchants via a convoluted trade route through Arabia and India, which in turn linked to its source in Spice Islands via Malacca. The Portuguese determined to break this chain and control the lucrative spice trade directly from its source. This led to the expansion of Portuguese sea exploration, pioneered by Vasco da Gama, into the east coasts of India that had resulted in the establishment of Portuguese stronghold in Calicut.  </p>
                <p>In April 1511, Alfonso de Albuquerque set sail from Goa to Malacca. They conquered the city on 24 August 1511. Following the 1511 conquest, the great Malay city port of Malacca passed into Portuguese hands and for the next 130 years remained under Portuguese governance despite incessant attempts by the former rulers of Malacca and other regional powers to dislodge the Europeans. Around the foot hill on which the Sultan's Istana once stood, the Portuguese built the stone fort known as A Famosa, completed in 1512. </p>
                <p>It soon became clear that Portuguese control of Malacca did not mean they now controlled Asian trade that centred on it. Their rule in Malacca was marred with difficulties. They could not become self-sufficient and remained highly dependent on Asian suppliers, as had their Malay predecessors. </p>
                
