                <h4> HISTORY </h4>
                <p> The origins of sepak takraw remains a matter of intense debate in Southeast Asia, as several countries proudly claim it as their own. There is, however, some agreement that the game was introduced to Southeast Asia through commercial contact with China, where an ancient form of the game originated. </p>
                <p> The modern version of sepak takraw is fiercely competitive and began taking shape in Thailand almost 200 years ago. In 1829, the Siam Sports Association drafted the first rules for the game. Four years later, the association introduced the volleyball-style net and held the first public contest. Within just a few years, sepak takraw was introduced to the Physical Training curriculum in schools. </p>
                <p> In Bangkok, the murals at Wat Phra Kaew (the Temple of the Emerald Buddha) depict the Hindu god Hanuman playing sepak takraw in a ring with a troop of fellow monkeys. Other historical accounts also mention the game earlier during the reign of King Naresuan of Ayutthaya. The game became such a cherished local custom that an exhibition of volleyball-style sepak takraw was staged to celebrate the kingdom's first constitution in 1933, the year after Thailand abolished absolute monarchy. </p>
                <p> Nowadays, sepak takraw is played on a modified badminton doubles court, with the net standing five feet above the ground. Each team consists of three players; left inside, right inside and back server. </p>
                <p> Almost every nation that played this game knew it by a different name. In Malaysia, Singapore and Brunei, it's called 'sepak raga', whereas in Thailand it's commonly known as 'takraw'. The same game goes by the name of 'sipa' in the Philippines, 'da cau' in Vietnam, 'rago' in Indonesia, and 'kator' in Laos. Since sepak takraw was played and enjoyed in several countries, there were a lot of inconsistencies in terms of how the game was played and judged. </p>
                <p> In 1960, representatives from Malaysia, Singapore, Indonesia, Lao and Thailand met in Kuala Lumpur to standardise rules and regulations for the game. And after a long and heated debate, consensus was reached that the sport would henceforth be officially called sepak takraw. </p>
                <p> They also formed the Asian Sepak Takraw Federation (ASTAF), and translated the rules into English, setting the stage for the first international competition, held in Malaysia in 1965, at the Southeast Asian Peninsular Games, or SEAP Games, the predecessor to today's Southeast Asian Games (SEA Games). This chain of events set the stage for the international development of sepak takraw. However, it was the replacement of the natural rattan ball, which tended to splinter and warp, with the more standardised synthetic plastic ball that really kicked the game's popularity into high gear. </p>
                <p> In 1990, sepak takraw was included at the Asian Games in Beijing. Women also got in on the action with the first women's championships in Thailand hosted in 1997. Today, more than 20 countries have national sepak takraw associations with representatives on the board of the global governing body, the International Sepak Takraw Federation (ISTAF). </p>
                <p> Sepak takraw is one of the fastest growing sports in Asia, as well as many other parts of the world. The gravity-defying kicks, contorted aerial twists, turns of the body and the blinding speed of play astound spectators worldwide. For most of the past decade, Thailand has dominated international competitions, winning nearly every major event. Malaysia turned the tide at the 2005 Manila SEA Games. </p>
                <p> Thailand and Malaysia will remain the teams to beat for the foreseeable future, but other sepak takraw powerhouses such as Myanmar, South Korea, Singapore and Vietnam are closing in quickly. Asian nations are currently lobbying to give the game Olympic Games, so as to attract wider audiences overseas and to give Asia a much-needed boost in the medals table - and it's only a matter of time before the region's beloved pastime takes its rightful place among other sports at the Olympic Games. </p>


