                <h4> HISTORY </h4>

                <h5> An ancient discipline </h5>
                <p> Prehistoric man learnt to swim in order to cross rivers and lakes – we know this because cave paintings from the Stone Age depicting swimmers have been found in Egypt. Swimming was also referred to in Greek mythology. </p>
                
                <h5> Olympic history </h5>
                <p> Swimming has featured on the programme of all editions of the Games since 1896. The very first Olympic events were freestyle (crawl) or breaststroke. Backstroke was added in 1904. </p>
                <p> In the 1940s, breaststrokers discovered that they could go faster by bringing both arms forward over their heads. This practice was immediately forbidden in breaststroke, but gave birth to butterfly, whose first official appearance was at the 1956 Games in Melbourne. This style is now one of the four strokes used in competition. </p>
                <p> Women’s swimming became Olympic in 1912 at the Stockholm Games. Since then, it has been part of every edition of the Games. The men’s and women’s programmes are almost identical, as they contain the same number of events, with only one difference: the freestyle distance is 800 metres for women and 1,500 metres for men. </p>


                <p>&nbsp;</p>
                <h4> Description</h4>
                <p> Swimming is the movement by humans or animals through water, usually without artificial assistance. Swimming is an activity that can be both useful and recreational. Its primary uses are bathing, cooling, travel, fishing, escape, and sport. </p>
                <p> Swimming, especially the "crawl" and "backstroke" modalities, is probably the best sport a healthy individual with an anatomically normal back can perform for his/her spine, since it tends to strengthen back muscles symmetrically. </p>
                <p> Nevertheless, unless the swimming style is skillful, "breaststroke" swimming may entail a certain degree of overburden on the cervico-dorsal musculature, especially if the head is constantly maintained above the water level, thus forcing the spine to arch backward. Also, the "butterfly" style tends to increase the curvature of the lumbar spine - "lordosis"-. The same occurs when competition swimmers excessively arch their back at the start line. To avoid pain resulting from these postures and strains, it is essential to perform specific exercises aimed at strengthening the abdominal and low back muscles. </p>
                <p> On the other hand, some swimming styles, otherwise advisable, may be contraindicated in some abnormalities, such as protrusions or disk herniations, while other spinal deformities recommend specific changes in the swimming style. In these instances, it is imperative to individualize the appropriate style after a thorough physical examination. </p>






