                <h4> HISTORY </h4>
                <p> Pin bowling has been played for thousands of years. The remains of artefacts resembling bowling balls and pins were discovered by famous English Egyptologist Sir Flinders Petrie during the first half of the twentieth century, in the grave of an Egyptian child, dated to approximately 5,200BC. In latter years, various rudimentary forms of the sport developed throughout Europe. Lawn bowling – in which balls were rolled at a target ball, much like the modern game of bowls – was particularly popular during the Middle Ages and played a pivotal role in the germination of modern day ten-pin bowling. The first mention of lawn bowls came in 1366, when King Edward III of England passed a decree outlawing the game because he feared his troops were neglecting their training to play. </p>
                <p> Another popular medieval pastime that had a large significance on ten-pin bowling was the game of skittles, or “kegelspiel” as it was known in Germany, the country in which it first developed. Players rolled small wooden balls at nine skittles, which were arranged in a diamond formation. In some regions, balls were thrown much like a bowler throws in cricket. During the 19th century, German settlers developed the game of kegel further in southern Australia, particularly in the Barossa Valley region, where it remains popular to this day. </p>
                <p> Meanwhile, skittles and other forms of nine-pin bowling were introduced into the Americas during the colonial era, when Western Europe was at the height of its power. German, English and Dutch settlers all brought their bowling games to the new world, where they became immediately popular. By 1841, nine-pin bowling had become so popular in the US state of Connecticut, New England, that the authorities were forced to outlaw the sport because of escalating problems with associated gambling, crime and violence. In order to circumvent the law, players quickly moved to introduce one extra pin, and also rearranged the rack into the now familiar triangle pattern. </p>
                <p> In the build-up to the American Civil War, the popularity of the game spread throughout the free United States in the north of the country, fostered by German migrants. By the 1800s, New York was nominated as the unofficial capital of bowling, and in 1840, the first indoor bowling alley – Knickerbockers – was built in the city. Once the Confederacy states had been subdued, ten-pin bowling continued to grow in stature throughout USA. Manufacturers also began investing into the new phenomenon, developing better equipment and mass produced balls. </p>
                <p> In 1888, John Brunswick, founder of the Brunswick Corporation, which made billiard tables, started producing bowling equipment. By 1914, he would introduce the Mineralite ball, the first to be made of hard rubber instead of wood. The ball was eagerly promoted throughout the world with a global tour, and was even displayed at the 1934 Century of Progress World Exposition in Chicago, Illinois. </p>

